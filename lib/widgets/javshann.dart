  void _nextPage() {
    setState(() {
      if (_pageNumber < _texts.length) {
        _pageNumber++;
      } else {
        _pageNumber = 1;
      }
      _appBarTitle = '$_pageNumber - баб';
      _text = _texts[_pageNumber - 1];
    });
  }

  void _previousPage() {
    setState(() {
      if (_pageNumber > 1) {
        _pageNumber--;
      } else {
        _pageNumber = _texts.length;
      }
      _appBarTitle = '$_pageNumber - баб';
      _text = _texts[_pageNumber - 1];
    });
  }

