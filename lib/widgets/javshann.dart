class _PagePickerDialog extends StatelessWidget {
  final int pageCount;
  final int currentPage;

  const _PagePickerDialog({required this.pageCount, required this.currentPage});

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;
    final textColor = isDark ? Colors.white : Colors.black;

    return AlertDialog(
      backgroundColor: isDark ? const Color(0xFF232228) : null,
      title: Text(
        'Баракты тандоо',
        style: GoogleFonts.nunito(
          color: textColor,
          fontWeight: FontWeight.bold,
        ),
      ),
      content: SingleChildScrollView(
        child: ListBody(
          children: List.generate(pageCount, (index) {
            final pageNumber = index + 1;
            return RadioListTile<int>(
              title: Text(
                '$pageNumber - баб',
                style: GoogleFonts.nunito(
                  color: textColor,
                  fontWeight: FontWeight.bold,
                ),
              ),
              value: pageNumber,
              groupValue: currentPage,
              activeColor: isDark ? Colors.orange : Color(0xff16423C),
              onChanged: (value) {
                Navigator.of(context).pop(value);
              },
            );
          }),
        ),
      ),
      actions: <Widget>[
        InkWell(
          borderRadius: BorderRadius.circular(30),
          onTap: () => Navigator.of(context).pop(),
          child: Container(
            padding: const EdgeInsets.all(8),
            child: Text(
              'Жокко чыгаруу',
              style: GoogleFonts.nunito(
                color: textColor,
                fontSize: 15,
                fontWeight: FontWeight.w600,
              ),
            ),
          ),
        ),
      ],
    );
  }
}
